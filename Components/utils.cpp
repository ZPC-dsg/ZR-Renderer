Utils::Utils() {
    genCube();
    genQuad();
}

Utils::Utils(const std::string& name)
    :m_main_scene(name)
{
    genCube();
    genQuad();
}

Utils::~Utils() {
    glBindVertexArray(0);
    glDeleteVertexArrays(1, &cubeVAO);
    glDeleteVertexArrays(1, &quadVAO);
    glBindBuffer(GL_ARRAY_BUFFER, 0);
    glDeleteBuffers(1, &cubeVBO);
    glDeleteBuffers(1, &quadVBO);
}

